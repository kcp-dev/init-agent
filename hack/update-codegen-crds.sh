set -euo pipefail

cd $(dirname $0)/..
source hack/lib.sh

CRD_DIR=deploy/crd
KCP_CRD_DIR="$CRD_DIR/kcp.io"
rm -rf -- "$KCP_CRD_DIR"
mkdir -p "$KCP_CRD_DIR"

echodate "Generating openAPI v3 CRDsâ€¦"

CONTROLLER_GEN="$(UGET_PRINT_PATH=relative make --no-print-directory install-controller-gen)"
YQ="$(UGET_PRINT_PATH=relative make --no-print-directory install-yq)"

"$CONTROLLER_GEN" \
  crd \
  paths=./sdk/apis/... \
  output:crd:dir=./$KCP_CRD_DIR

beautify() {
  "$YQ" --inplace --no-doc 'del(.metadata.creationTimestamp)' "$1"

  mv "$1" "$1.bak"
  echo -e "# This file has been generated by hack/update-codegen-crds.sh, DO NOT EDIT.\n" > "$1"
  cat "$1.bak" >> "$1"
  rm "$1.bak"
}

# beautify CRDs just because we can
for f in $KCP_CRD_DIR/*.yaml; do
  beautify "$f"
done
